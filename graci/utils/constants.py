"""Module for storing numeric constants"""

# ENERGY CONVERSIONS
# convert au to electron volts
au2ev    = 27.2113845
ev2au    = 1. / au2ev
# convert au to cm-1
au2cm    = 219474.63068
cm2au    = 1. / au2cm

# DISTANCE CONVERSIONS
# bohr to angstrom
bohr2ang = 0.529177249
# angstrom to bohr
ang2bohr  = 1. / bohr2ang
